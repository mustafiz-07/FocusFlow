// ─────────────────────────────────────────────────────────────
//  ASSETS SETUP — READ ME
// ─────────────────────────────────────────────────────────────
//
//  In Xcode, open Assets.xcassets and add these Color Sets:
//
//  1. "BackgroundColor"
//      Any Appearance  → #12121f
//      Dark            → #12121f
//
//  2. "CardBackground"
//      Any Appearance  → rgba(255,255,255, 0.06)  (white 6% opacity)
//      Dark            → rgba(255,255,255, 0.06)
//
//  3. "AccentColor"  (already exists in new projects)
//      Any Appearance  → #FF6B00  (orange)
//      Dark            → #FF6B00
//
// ─────────────────────────────────────────────────────────────
//  LOCAL AUDIO FILES
// ─────────────────────────────────────────────────────────────
//  Add these .mp3 or .wav files to your Xcode project:
//      rain.mp3
//      forest.mp3
//      cafe.mp3
//      fire.mp3
//      ocean.mp3
//      white_noise.mp3
//      bell.mp3          ← used as the timer alarm
//
//  You can source these from:
//  - https://freesound.org  (free with account)
//  - https://pixabay.com/music/  (free)
//  - Or generate with online tools
//
//  Drag all .mp3 files into Xcode → check "Copy items if needed"
//  → ensure they're added to the FocusFlow target
//
// ─────────────────────────────────────────────────────────────
//  APP ICONS
// ─────────────────────────────────────────────────────────────
//  Add your app icon to Assets.xcassets → AppIcon
//  Minimum required sizes:
//      1024x1024 (App Store)
//      The rest are auto-generated in Xcode 14+
//
